void initLevel10(){
  nextLevel = 11;
  gamerunning = 1;
  
  player[0] = 120;
  player[1] = 96;
  
 // scroll_bkg(0, 4);

  init();

  //Create temporary level array
  UINT16 mapLevel[level10MapLength];

  // Copy level map to a temp array that will be used in game
  // The original map need to be unchanged so we can reset the level    
  memcpy(mapLevel, level10Map, level10MapLength);
    
  // Load map 
  set_bkg_tiles(0, 0, level10Width, level10Height, mapLevel); // Load Level

  while (gamerunning)
  {
      checkInput(mapLevel, level10Width); // Check for user input (and act on it)
      updateSwitches(); // Make sure the SHOW_SPRITES and SHOW_BKG switches are on each loop
      wait_vbl_done(); // Wait until VBLANK to avoid corrupting memory
  }
}
