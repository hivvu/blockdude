void initLevel8(){
  nextLevel = 9;
  gamerunning = 1;
  
  player[0] = 168;
  player[1] = 128;
  

  init();

  //Create temporary level array
  UINT16 mapLevel[level8MapLength];

  // Copy level map to a temp array that will be used in game
  // The original map need to be unchanged so we can reset the level    
  memcpy(mapLevel, level8Map, level8MapLength);
    
  // Load map 
  set_bkg_tiles(0, 0, level8Width, level8Height, mapLevel); // Load Level

  while (gamerunning)
  {
      checkInput(mapLevel, level8Width); // Check for user input (and act on it)
      updateSwitches(); // Make sure the SHOW_SPRITES and SHOW_BKG switches are on each loop
      wait_vbl_done(); // Wait until VBLANK to avoid corrupting memory
  }
}
