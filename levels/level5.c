void initLevel5(){
  nextLevel = 6;
  gamerunning = 1;
  
  player[0] = 104;
  player[1] = 80;
  

  init();

  //Create temporary level array
  UINT16 mapLevel[level5MapLength];

  // Copy level map to a temp array that will be used in game
  // The original map need to be unchanged so we can reset the level    
  memcpy(mapLevel, level5Map, level5MapLength);
    
  // Load map 
  set_bkg_tiles(0, 0, level5Width, level5Height, mapLevel); // Load Level

  while (gamerunning)
  {
      checkInput(mapLevel, level5Width); // Check for user input (and act on it)
      updateSwitches(); // Make sure the SHOW_SPRITES and SHOW_BKG switches are on each loop
      wait_vbl_done(); // Wait until VBLANK to avoid corrupting memory
  }
}
